    function viewReportDetails(content) {
      try {
        // Parse JSON content
        const parsedContent = JSON.parse(content);
        const modal = document.getElementById('reportModal');
        const details = document.getElementById('reportDetails');
        let html = '';

        // Handle report content (from generate.blade.php)
        if (parsedContent.intro && parsedContent.table) {
          html += `
            <p><strong>Title:</strong> ${parsedContent.intro.title || 'N/A'}</p>
            <p><strong>Report Type:</strong> ${parsedContent.intro.report_type || 'N/A'}</p>
            <p><strong>Selected Prisons:</strong> ${parsedContent.intro.selected_prisons || 'N/A'}</p>
            <p><strong>Generated Date:</strong> ${parsedContent.intro.generated_date || 'N/A'}</p>
          `;
          if (Array.isArray(parsedContent.table)) {
            html += '<table class="pims-modal-table">';
            parsedContent.table.forEach((row, index) => {
              html += '<tr>';
              row.forEach(cell => {
                const tag = index === 0 ? 'th' : 'td';
                html += `<${tag}>${cell || 'N/A'}</${tag}>`;
              });
              html += '</tr>';
            });
            html += '</table>';
          } else {
            html += '<p>No table data available.</p>';
          }
        }
        // Handle navigation tracking (e.g., "Viewed reports list")
        else if (parsedContent.action) {
          html += `<p><strong>Action:</strong> ${parsedContent.action || 'N/A'}</p>`;
          html += `<p><strong>Timestamp:</strong> ${parsedContent.timestamp || 'N/A'}</p>`;
        }
        // Fallback for unexpected content
        else {
          html += '<p>Unknown report content format.</p>';
        }

        details.innerHTML = html;
        modal.style.display = 'flex';
      } catch (error) {
        console.error('Error parsing report content:', error.message);
        document.getElementById('reportDetails').innerHTML = '<p class="pims-error-message">Failed to load report details.</p>';
        document.getElementById('reportModal').style.display = 'flex';
      }
    }

    function closeModal() {
      document.getElementById('reportModal').style.display = 'none';
      document.getElementById('reportDetails').innerHTML = '';
    }

    // Close modal when clicking outside
    window.onclick = function(event) {
      const modal = document.getElementById('reportModal');
      if (event.target === modal) {
        closeModal();
      }
    };
  </script>
